// Apply theme to document
export const applyTheme = (themeName) => {
  const theme = themes[themeName];
  if (!theme) return;

  const root = document.documentElement;
  Object.entries(theme.colors).forEach(([property, value]) => {
    root.style.setProperty(property, value);
  });

  localStorage.setItem("bookalink-theme", themeName);
};

// Get stored theme from localStorage
export const getStoredTheme = () => {
  return localStorage.getItem("bookalink-theme") || "coral";
};

// Load saved theme on app start
export const loadSavedTheme = () => {
  const savedTheme = getStoredTheme();
  applyTheme(savedTheme);
  return savedTheme;
};

// Get theme key from database theme_color value
export const getThemeKeyFromColor = (color) => {
  if (!color) return "coral";

  // Find theme by matching primary color
  const themeEntry = Object.entries(themes).find(
    ([key, theme]) =>
      theme.colors["--primary-color"]?.toLowerCase() === color?.toLowerCase()
  );

  return themeEntry ? themeEntry[0] : "coral";
};

// Apply theme from seller's database theme_color
export const applySellerTheme = (themeColor) => {
  if (!themeColor) {
    loadSavedTheme();
    return;
  }

  const themeKey = getThemeKeyFromColor(themeColor);
  applyTheme(themeKey);
};
